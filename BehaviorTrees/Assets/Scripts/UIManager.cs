        foreach (Task task in parent.children)
        {
            TaskBlock taskBlock = Object.Instantiate(taskBlockPrefab, taskBlockParent.transform).GetComponent<TaskBlock>();
            output.Add(taskBlock);
            taskBlocks.Add(taskBlock);
